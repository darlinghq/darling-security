static void tests(void)
{
    SecCertificateRef leaf_cert;
    SecCertificateRef CA_cert;

    // Import certificates from byte array above
    isnt(leaf_cert = SecCertificateCreateWithBytes(NULL, leaf_certificate, sizeof(leaf_certificate)),
         NULL, "Leaf Cert");
    isnt(CA_cert   = SecCertificateCreateWithBytes(NULL, CA_certificate, sizeof(CA_certificate)),
         NULL, "CA Cert");

    /*
     *  1) Test explicit revocation with no OCSP/CRL
     */

    OSStatus status;
    SecPolicyRef policy_ssl_default = SecPolicyCreateSSL(true, CFSTR("www.paypal.com"));
    SecPolicyRef policy_revoc_disabled = SecPolicyCreateRevocation(kSecRevocationNetworkAccessDisabled);

    // Default Policies
    CFMutableArrayRef DefaultSSLPolicy = CFArrayCreateMutable(NULL, 0, &kCFTypeArrayCallBacks);
    CFArrayAppendValue(DefaultSSLPolicy, policy_ssl_default);

    // Default Policies + explicit revocation disabled
    CFMutableArrayRef DefaultSSLPolicyWithNoRevocation = CFArrayCreateMutable(NULL, 0, &kCFTypeArrayCallBacks);
    CFArrayAppendValue(DefaultSSLPolicyWithNoRevocation, policy_ssl_default);
    CFArrayAppendValue(DefaultSSLPolicyWithNoRevocation, policy_revoc_disabled);

    // Valid chain of Cert (leaf + CA)
    CFMutableArrayRef CertFullChain = CFArrayCreateMutable(NULL, 0, &kCFTypeArrayCallBacks);
    CFArrayAppendValue(CertFullChain, leaf_cert);
    CFArrayAppendValue(CertFullChain, CA_cert);

    // Chain of cert minus the issuer
    CFMutableArrayRef CertMissingIssuer = CFArrayCreateMutable(NULL, 0, &kCFTypeArrayCallBacks);
    CFArrayAppendValue(CertMissingIssuer, leaf_cert);

    // Free Resources since all are in arrays
    CFReleaseSafe(leaf_cert);
    CFReleaseSafe(CA_cert);
    CFReleaseSafe(policy_ssl_default);
    CFReleaseSafe(policy_revoc_disabled);

    // a) First evaluate an entire EV certificate chain with default policy
    // OCSP/CRL performed (online/from cache)

    // Array of policy to add explicit revocation policy
    {
        SecTrustRef trust = NULL;
        SecTrustResultType trust_result;

        // Proceed to trust evaluation in two steps
        ok_status(status = SecTrustCreateWithCertificates(CertFullChain, DefaultSSLPolicy, &trust),
                  "SecTrustCreateWithCertificates");
        ok_status(status = SecTrustEvaluate(trust, &trust_result), "SecTrustEvaluate");

        // Check results
        is_status(trust_result, kSecTrustResultUnspecified, "trust is kSecTrustResultUnspecified");
        CFDictionaryRef TrustResultsDict = SecTrustCopyResult(trust);
        CFBooleanRef ev = (CFBooleanRef)CFDictionaryGetValue(TrustResultsDict,
                                                             kSecTrustExtendedValidation);
        ok(ev && CFEqual(kCFBooleanTrue, ev), "extended validation succeeded");

        CFReleaseNull(TrustResultsDict);
        CFReleaseNull(trust);
    }

    // b) Set explicit revocation policy to disable revocation checking,
    // and now expect EV marker to be dropped.
    // Network packet logging can be used to confirm no OCSP/CRL message is sent.
    {
        SecTrustRef trust = NULL;
        SecTrustResultType trust_result;

        // Proceed to trust evaluation in two steps
        ok_status(status = SecTrustCreateWithCertificates(CertFullChain, DefaultSSLPolicyWithNoRevocation, &trust),
                  "SecTrustCreateWithCertificates");
        ok_status(status = SecTrustEvaluate(trust, &trust_result), "SecTrustEvaluate");

        // Check results
        is_status(trust_result, kSecTrustResultUnspecified, "trust is kSecTrustResultUnspecified");
        CFDictionaryRef TrustResultsDict = SecTrustCopyResult(trust);
        CFBooleanRef ev = (CFBooleanRef)CFDictionaryGetValue(TrustResultsDict,
                                                             kSecTrustExtendedValidation);
        // With SecTrust Unification, the OCSP response is cached by the previous evaluation.
        // FIXME The semantics of the input to SecPolicyCreateRevocation are technically not honored,
        // since if neither the OCSP or CRL bits are set, we should not be using either. Unfortunately,
        // the iOS implementation treats this as a no-op, which for EV certs means an OCSP check by default.

        ok(ev && CFEqual(kCFBooleanTrue, ev), "Expect success even if unable to use network, due to caching");

        CFReleaseNull(TrustResultsDict);
        CFReleaseNull(trust);
    }

    /*
     *  2) Test retrieving of issuer being blocked
     */

    // a) Evaluate leaf EV certificate and expect success (issuer retrieved online)
    {
        SecTrustRef trust = NULL;
        SecTrustResultType trust_result;

        // Proceed to trust evaluation in two steps
        ok_status(status = SecTrustCreateWithCertificates(CertMissingIssuer, DefaultSSLPolicy, &trust),
                  "SecTrustCreateWithCertificates");
        ok_status(status = SecTrustSetNetworkFetchAllowed(trust,true), "SecTrustSetNetworkFetchAllowed");
        ok_status(status = SecTrustEvaluate(trust, &trust_result), "SecTrustEvaluate");

        // Check results
        is_status(trust_result, kSecTrustResultUnspecified, "trust is kSecTrustResultUnspecified");
        CFDictionaryRef TrustResultsDict = SecTrustCopyResult(trust);
        CFBooleanRef ev = (CFBooleanRef)CFDictionaryGetValue(TrustResultsDict,
                                                             kSecTrustExtendedValidation);
        ok(ev && CFEqual(kCFBooleanTrue, ev), "extended validation succeeded");

        CFReleaseNull(TrustResultsDict);
        CFReleaseNull(trust);
    }

    // b) Set SecTrustSetNetworkFetchAllowed to false which should prevent issuer cert to be fetched
    // and therefore cause evaluation failure.
    {
        SecTrustRef trust = NULL;
        SecTrustResultType trust_result;

        // Proceed to trust evaluation in two steps, forcing no network allowed
        ok_status(status = SecTrustCreateWithCertificates(CertMissingIssuer, DefaultSSLPolicy, &trust),
                  "SecTrustCreateWithCertificates");
        ok_status(status = SecTrustSetNetworkFetchAllowed(trust,false), "SecTrustSetNetworkFetchAllowed");
        ok_status(status = SecTrustEvaluate(trust, &trust_result), "SecTrustEvaluate");

        // Check results
        // with SecTrust Unification, the issuing cert may or may not be cached from the previous test
        if (trust_result == kSecTrustResultUnspecified)
            trust_result = kSecTrustResultRecoverableTrustFailure;
        is_status(trust_result, kSecTrustResultRecoverableTrustFailure, "trust is kSecTrustResultRecoverableTrustFailure");

        CFReleaseNull(trust);
    }

    /*
     *  3) Test verification requiring a positive response from the revocation server
     */

    SecCertificateRef smime_leaf_cert;
    SecCertificateRef smime_CA_cert;
    SecCertificateRef smime_root_cert;

    // Import certificates from byte array above
    isnt(smime_leaf_cert = SecCertificateCreateWithBytes(NULL, smime_leaf_certificate, sizeof(smime_leaf_certificate)),
         NULL, "SMIME Leaf Cert");
    isnt(smime_CA_cert   = SecCertificateCreateWithBytes(NULL, smime_CA_certificate, sizeof(smime_CA_certificate)),
         NULL, "SMIME CA Cert");
    isnt(smime_root_cert = SecCertificateCreateWithBytes(NULL, smime_root_certificate, sizeof(smime_root_certificate)),
         NULL, "SMIME Root Cert");

    SecPolicyRef smimePolicy = SecPolicyCreateWithProperties(kSecPolicyAppleSMIME, NULL);
    SecPolicyRef revocPolicy = SecPolicyCreateRevocation(kSecRevocationUseAnyAvailableMethod | kSecRevocationRequirePositiveResponse);
    isnt(smimePolicy, NULL, "SMIME Policy");
    isnt(revocPolicy, NULL, "SMIME Revocation Policy");

    // Default Policies
    CFMutableArrayRef SMIMEDefaultPolicy = CFArrayCreateMutable(NULL, 0, &kCFTypeArrayCallBacks);
    CFArrayAppendValue(SMIMEDefaultPolicy, smimePolicy);

    // Default Policies + explicit revocation
    CFMutableArrayRef SMIMEDefaultPolicyWithRevocation = CFArrayCreateMutable(NULL, 0, &kCFTypeArrayCallBacks);
    CFArrayAppendValue(SMIMEDefaultPolicyWithRevocation, smimePolicy);
    CFArrayAppendValue(SMIMEDefaultPolicyWithRevocation, revocPolicy);

    // Valid chain of Cert (leaf + CA)
    CFMutableArrayRef SMIMECertChain = CFArrayCreateMutable(NULL, 0, &kCFTypeArrayCallBacks);
    CFArrayAppendValue(SMIMECertChain, smime_leaf_cert);
    CFArrayAppendValue(SMIMECertChain, smime_CA_cert);

    // Valid anchor certs
    CFMutableArrayRef SMIMEAnchors = CFArrayCreateMutable(NULL, 0, &kCFTypeArrayCallBacks);
    CFArrayAppendValue(SMIMEAnchors, smime_root_cert);

    // Free Resources contained in arrays
    CFReleaseSafe(smime_leaf_cert);
    CFReleaseSafe(smime_CA_cert);
    CFReleaseSafe(smime_root_cert);
    CFReleaseSafe(smimePolicy);
    CFReleaseSafe(revocPolicy);

    CFDateRef VerifyDate;
    isnt(VerifyDate = CFDateCreate(NULL, 332900000.0), NULL, "Create verify date");

    // Standard evaluation for the given verify date
    {
        SecTrustRef trust = NULL;
        SecTrustResultType trust_result;

        ok_status(status = SecTrustCreateWithCertificates(SMIMECertChain, SMIMEDefaultPolicy, &trust),
                  "SecTrustCreateWithCertificates");
        ok_status(SecTrustSetVerifyDate(trust, VerifyDate), "Set date");
        ok_status(SecTrustSetAnchorCertificates(trust, SMIMEAnchors), "Set anchors");

        ok_status(status = SecTrustEvaluate(trust, &trust_result), "SecTrustEvaluate");

        // Check results
        // %%% This is expected to fail, since the "TC TrustCenter Class 1 L1 CA IX" CA is revoked.
        // While it is still present in the CRL, we no longer check revocation for it because the root
        // "TC TrustCenter Universal CA I" is not system-trusted. Accept either failure mode here.
        ok((trust_result == kSecTrustResultRecoverableTrustFailure) ||
           (trust_result == kSecTrustResultFatalTrustFailure), "trust result failure");

        CFReleaseNull(trust);
    }

    // Revocation-required evaluation should fail, since this CA's servers no longer exist
    // and no valid responses are available
    {
        SecTrustRef trust = NULL;
        SecTrustResultType trust_result;

        ok_status(status = SecTrustCreateWithCertificates(SMIMECertChain, SMIMEDefaultPolicyWithRevocation, &trust),
                  "SecTrustCreateWithCertificates");
        ok_status(SecTrustSetVerifyDate(trust, VerifyDate), "Set date");
        ok_status(SecTrustSetAnchorCertificates(trust, SMIMEAnchors), "Set anchors");

        ok_status(status = SecTrustEvaluate(trust, &trust_result), "SecTrustEvaluate");

        // Check results
        // %%% This is expected to fail, since the "TC TrustCenter Class 1 L1 CA IX" CA is revoked.
        // While it is still present in the CRL, we no longer check revocation for it because the root
        // "TC TrustCenter Universal CA I" is not system-trusted. Accept either failure mode here.
        ok((trust_result == kSecTrustResultRecoverableTrustFailure) ||
           (trust_result == kSecTrustResultFatalTrustFailure), "trust result failure");

        CFReleaseNull(trust);
    }

    // Free remaining resources
    CFReleaseSafe(DefaultSSLPolicy);
    CFReleaseSafe(DefaultSSLPolicyWithNoRevocation);
    CFReleaseSafe(CertFullChain);
    CFReleaseSafe(CertMissingIssuer);

    CFReleaseSafe(VerifyDate);
    CFReleaseSafe(SMIMEDefaultPolicy);
    CFReleaseSafe(SMIMEDefaultPolicyWithRevocation);
    CFReleaseSafe(SMIMECertChain);
    CFReleaseSafe(SMIMEAnchors);
}

